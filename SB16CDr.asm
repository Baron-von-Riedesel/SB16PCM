ResetDSP proc
	mov dx, [wBase]
	add dx, SB_DSPRESET
	mov al, 1
	out dx, al			; start DSP reset

	in al, dx
	in al, dx
	in al, dx
	in al, dx			; wait 3 Šsec

	xor al, al
	out dx, al			; end DSP Reset

	mov dx, [wBase]
	add dx, SB_DSPSTATUS
	WaitRead
	mov dx, [wBase]
	add dx, SB_DSPREAD
	in al, dx
	cmp al, 0aah		; if there is a SB then it returns 0AAh
	je @F
	stc
	ret
@@:
	clc
	ret
ResetDSP endp

ReadDSPWord proc stdcall bCmd:byte

	WriteDSP [wBase], bCmd
	mov dx, [wBase]
	add dx, SB_DSPSTATUS
	WaitRead
	mov dx, [wBase]
	add dx, SB_DSPREAD
	in al, dx
	mov ah, al
	mov dx, [wBase]
	add dx, SB_DSPSTATUS
	WaitRead
	mov dx, [wBase]
	add dx, SB_DSPREAD
	in al, dx
	ret

ReadDSPWord endp

;--- convert redbook address in EAX to LBA

CvtLBA proc
	mov cx,ax		;Save "seconds" & "frames" in CX-reg.
	shr eax,16		;"minute" value to AX
	cmp ax,99		;Is "minute" value too large?
	ja error
	cmp ch,60		;Is "second" value too large?
	ja error
	cmp cl,75		;Is "frame" value too large?
	ja error

;--- convert minute value to seconds
	mov edx,60
	mul dl
	mov dl,ch		;add "second" value.
	add ax,dx		;now ax contains seconds

;--- convert seconds to frames
	mov dl,75
	mul edx
	mov dl,150		;add "frame" value by subtracting it from "2 sec" offset
	sub dl,cl
	sub eax,edx
	ret
error:
	mov eax,100*60*75	;error, set value to max (450.000)
	ret
CvtLBA endp

