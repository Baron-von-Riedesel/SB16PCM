;──────────────────────────────────────────────────────────────────────────────
; Our IRQ handler for detecting end of playing
; It's generated by the SoundBlaster hardware
;──────────────────────────────────────────────────────────────────────────────
sbirqproc proc
	push ax
	push dx
	push ds
	mov ds, cs:[dsseg]
	mov [bReady], 1
if 0	; debugging
	push bx
	mov bh,0
	mov ax,0E00h or '.'
	int 10h
	pop bx
endif
	mov dx, [wBase]
if 1
	mov ax, SB_DSPINTACK	; VSB is happy with 22Eh or 22Fh, but real SB needs 22Eh for 8 bit!
	cmp wDmaBaseChn, 10h
	jb @F
endif
	mov ax, SB16_DSPINTACK
@@:
	add dx, ax
	in al, dx
	mov al, 020h
	cmp [wIrq], 8
	jb @F
	out 0A0h, al
@@:
	out 020h, al
	pop ds
	pop dx
	pop ax
	sti
	IRET
sbirqproc endp

GetEnvironmentVariable proc stdcall uses es si di pVar:ptr, pOut:ptr, wSize:word

	mov ah, 51h
	int 21h
	mov es, bx
	mov es, es:[2Ch]
	xor di, di
	mov si, pVar
	mov bx, si
	.while byte ptr [bx]
		inc bx
	.endw
	sub bx, si
nextvar:
	mov cx, bx
	push si
	push di
	repz cmpsb
	jz found
skipvar:
	pop di
	pop si
	mov al, 0
	or cx,-1
	repnz scasb
	cmp byte ptr es:[di],0
	jnz nextvar
	xor ax, ax
	ret
found:
	cmp byte ptr es:[di],'='
	jnz skipvar
	add sp, 2*2
	inc di
	mov si, di
	mov di, pOut
	mov cx, wSize
	mov bx, ds
	push es
	pop ds
	mov es, bx
@@:
	lodsb
	stosb
	cmp al,0
	loopnz @B
	mov ds, bx
	mov ax, di
	sub ax, pOut
	ret
GetEnvironmentVariable endp

;--- convert string to number
;--- si:src text
;--- cl:base (10/16)
;--- out:number in EAX
;---     si -> behind number
;--- Z if no valid digit found

getnum proc uses bx
	mov bx, si
	xor edx, edx
	movzx ecx, cl
nextdigit:
	lodsb
	sub al, '0'
	jb exit
	cmp al, 9
	jbe ok
	cmp cl, 16
	jnz exit
	sub al, 7
	cmp al, 15
	ja exit
ok:
	imul edx, ecx
	movzx eax, al
	add edx, eax
	jmp nextdigit
exit:
	dec si
	mov eax, edx
	cmp si, bx
	ret
getnum endp

;--- scan BLASTER variable, overwrite
;--- default values in wBase, wIrq, wDmaL, wDmaH, wType

ScanBlasterVar proc stdcall uses si pVar:ptr
	mov si, pVar
	or bx, -1
	.while (byte ptr [si])
		lodsb
		.if (al == ' ')
			or bx,-1
			.continue
		.endif
		.if ((al >= 'a') && (al <= 'z'))
			sub al,20h
		.endif
		.if (bx == -1)
			mov cl,16
			.if (al == 'A')
				mov bx, offset wBase
			.elseif (al == 'I')
				mov bx, offset wIrq
				mov cl,10
			.elseif (al == 'D')
				mov bx, offset wDmaL
			.elseif (al == 'H')
				mov bx, offset wDmaH
			.elseif (al == 'T')
				mov bx, offset wType
			.endif
			.continue
		.endif
		dec si
		call getnum
		jz numdone
		.if (bx != -1)
			mov [bx], ax
		.endif
numdone:
	.endw
	ret
ScanBlasterVar endp

;--- send device driver request

SendReq proc stdcall req:ptr BYTE

	mov bx,req
	push ds
	pop es
	mov cx,wDrive
	clc 			;XP needs this
	mov ax,1510h	;send dev. req.
	int 2Fh
	ret

SendReq endp

;--- check CD.
;--- returns: NC: ok
;---           C: no audio CD

ioctl08 struct	;get volume size
cmd     db ?
sectors dd ?	;size in sectors
ioctl08 ends

getvolsize proc

local ctl08:ioctl08

;--- get volume size
	mov req03.len, sizeof cmd03
	mov req03.cmd, IOCREAD
	lea ax, ctl08
	mov word ptr req03.taddr+0, ax
	mov word ptr req03.taddr+2, ss
	mov req03.numbyt, sizeof ioctl08
	mov ctl08.cmd, IO_VOLSIZE	;get volume size
	mov ctl08.sectors, 0
	invoke SendReq, addr req03
	jc error
	test req03.status, 8000h
	jnz error
	.if bVerbose
		invoke printf, CStr("IOCTL volume size: [%X] sectors=%lu",lf), req03.status, ctl08.sectors
	.endif
	mov eax, ctl08.sectors
	clc
	ret
error:
	invoke printf, CStr("IOCTL volume size: failed [%X]",lf), req03.status
	mov eax, -1
	clc
	ret

getvolsize endp

ioctl10 struct	;audio disk info
cmd     db ?
first   db ?
last    db ?
union
leadout dd ?
struct
leadout_f db ?
leadout_s db ?
leadout_m db ?
ends
ends
ioctl10 ends

getdiskinfo proc

local ctl10:ioctl10

	mov req03.len, sizeof cmd03
	mov req03.cmd, IOCREAD
	lea ax, ctl10
	mov word ptr req03.taddr+0, ax
	mov word ptr req03.taddr+2, ss
	mov req03.numbyt, sizeof ioctl10
	mov ctl10.cmd, IO_DISKINFO
	invoke SendReq, addr req03
	jc error
	test req03.status, 8000h
	jnz error
	.if bVerbose
		invoke printf, CStr("IOCTL disk info: [%X] tracks=%u-%u, leadout=%02u:%02u:%02u",lf),
			req03.status, ctl10.first, ctl10.last, ctl10.leadout_m, ctl10.leadout_s, ctl10.leadout_f
	.endif
	mov al, ctl10.first
	mov ah, ctl10.last
	clc
	ret
error:
	invoke printf, CStr("IOCTL disk info: failed [%X]",lf), req03.status
	stc
	ret

getdiskinfo endp

ioctl11 struct	;audio track info
cmd     db ?
track   db ?
union
start   dd ?
struct
start_f db ?
start_s db ?
start_m db ?
ends
ends
ctlinfo db ?    ; bit 6=1 -> data track
ioctl11 ends

gettrackinfo proc stdcall track:word

local ctl11:ioctl11

	mov req03.len, sizeof cmd03
	mov req03.cmd, IOCREAD
	lea ax, ctl11
	mov word ptr req03.taddr+0, ax
	mov word ptr req03.taddr+2, ss
	mov req03.numbyt, sizeof ioctl11
	mov ctl11.cmd, IO_TRKINFO
	mov ax, track
	mov ctl11.track, al
	invoke SendReq, addr req03
	jc error
	test req03.status, 8000h
	jnz error
	test ctl11.ctlinfo, 40h
	jnz error2
	mov eax, ctl11.start
	call CvtLBA   ; convert RedBook in EAX to LBA
	.if bVerbose
		push eax
		mov ecx, eax
		invoke printf, CStr("IOCTL track info(%u): [%X] start=%lu (RedBook=%02u:%02u:%02u)",lf),
        	track, req03.status, ecx, ctl11.start_m, ctl11.start_s, ctl11.start_f
		pop eax
	.endif
	ret
error:
	invoke printf, CStr("IOCTL track info(%u): failed [%X]",lf), track, req03.status
	stc
	ret
error2:
	invoke printf, CStr("IOCTL track info(%u): [%X] data track",lf), track, req03.status
	stc
	ret
gettrackinfo endp

;--- read CD
;--- out: NC: read ok, eax=bytes read

cdread proc stdcall pBuffer:ptr

	mov dx, pBuffer
	test dwChunks, 1
	jz @F
	add dx, SAMPLEBUFFERLENGTH shr 1
@@:
	mov req80.len,sizeof cmd80
	mov req80.subunit,00
	mov req80.cmd,READLONG
	mov req80.mode,HSG_MODE
	mov word ptr req80.address+0, dx
	mov word ptr req80.address+2, ds
	mov word ptr req80.numsecs+0, (SAMPLEBUFFERLENGTH shr 1) / ?SECSIZE
	mov word ptr req80.readmod, RAW

	invoke SendReq, addr req80
	jc error
	test req80.status, 8000h
	jnz error
	add req80.stasecs, (SAMPLEBUFFERLENGTH shr 1) / ?SECSIZE
	inc dwChunks
	movzx eax, ax
	ret
error:
	invoke printf, CStr("Read long(%lu): failed [%X]",lf), req80.stasecs, req80.status
	stc
	ret
cdread endp

;--- set DMA registers
;--- WRITEMASK      DMABase + 10 * DMAWidth
;--- WRITEMODE      DMABase + 11 * DMAWidth
;--- CLEARFLIPFLOP  DMABase + 12 * DMAWidth
;--- BASE_CHN       DMABase + DMAWidth * channel
;--- COUNT_CHN      DMABase + DMAWidth * channel + DMAWidth
;--- PAGE_CHN       channel PAGE

setdmaports proc uses ebx

	movzx ebx, wDmaH
	mov edx, 0	; edx=DMABase (0/C0)
	mov ecx, 1	; ecx=DMAWidth (1/2)
	cmp ebx, 4
	jb @F
	mov dx, 0C0h
	inc ecx
@@:
	mov eax, 10			; single mask register
	imul eax, ecx
	add eax, edx
	mov wDmaWriteMask, ax ; bits 0-1 select channel, bit 2 sets status
	mov ax, 11			; mode register
	imul eax, ecx
	add eax, edx
	mov wDmaWriteMode, ax
	mov ax, 12			; flip flop
	imul eax, ecx
	add eax, edx
	mov wDmaClearFlipFlop, ax
	mov al, [ebx+pgtab]	; al=page register
	mov ah, 0
	mov wDmaPageChn, ax
	mov eax, ebx
	and al, 3
	shl eax, 1
	imul eax, ecx
	add eax, edx
	mov wDmaBaseChn, ax	; address register
	add eax, ecx
	mov wDmaCntChn, ax	; cnt register
	ret
setdmaports endp

GetDmaChannel proc
	mov al, byte ptr wDmaH
	sub al, 4
	ret
GetDmaChannel endp

;--- get linear address and offset of sample buffer.
;--- (must not cross a 64-kB boundary)
;--- linear address (=dwSampleBuffer) is used for DMAcontroller page & offset
;--- offset address (=pSampleBuffer) is used for file reads

getsamplebufferaddr proc uses esi

	mov ax, ds
	movzx eax,ax
	shl eax, 4
	add eax, offset samplebuffer

	mov esi, eax
	mov ecx, SAMPLEBUFFERLENGTH
	lea eax, [esi+ecx-1]
	mov edx, esi
	shr eax, 16
	shr edx, 16
	cmp ax, dx				;does a 64kb segment overrun occur?
	jz @F

	shl eax, 16
	mov edx, eax			;eax & edx = linear address of 64kb segment to use
	sub edx, esi			;edx = difference to linear address of original sample buffer
	add [pSampleBuffer], dx	;adjust offset

	mov esi, eax
@@:
	mov eax, esi

	mov [dwSampleBuffer], eax
	clc
	ret

getsamplebufferaddr endp

;--- return #samples in one half of sample buffer (ecx).
;--- this will be the value for the DSP play cmd.
;--- stereo are 2 samples, so just the bits/sample are relevant.
;--- edx must be preserved.

getsamplebufferlength proc

	mov ecx, SAMPLEBUFFERLENGTH shr 1
	shr ecx, 1
	ret
getsamplebufferlength endp

