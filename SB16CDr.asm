;--- init SB hardware, including ISA DMA

InitSB proc

	cmp bInit, 1
	jz exit    

;--- init SB and DMA hardware

	call ResetDSP
	.if CARRY?
		invoke printf, CStr('No SoundBlaster found at 0x%x',lf), [wBase]
		stc
		ret
	.endif

;--- check if it's a SB16
	invoke ReadDSPWord, DSP_VERSION
	.if ax < 400h
		invoke printf, CStr('No SB16 found, DSP version=%X',lf), ax
		stc
		ret
	.endif

	mov bInit, 1

;--- setup isr

	WriteDSP [wBase], DSP_ENABLESPEAKER

	mov al, byte ptr [wIrq]
	.if ( al < 8 )
		add al, 8
	.else
		add al, 68h
	.endif
	push es
	mov ah, 35h
	int 21h
	mov word ptr [OldIntSB+0], bx
	mov word ptr [OldIntSB+2], es
	pop es
	push ds
	push cs
	pop ds
	mov dx, offset sbirqproc
	mov ah, 25h
	int 21h
	pop ds

;--- enable sound IRQ

	in al, 021h
	mov ah, al
	in al, 0A1h
	xchg al,ah
	mov wOldMask, ax

	mov dx, 21h
	mov bx, [wIrq]
	cmp bx, 8
	jb @F
	mov dx, 0A1h
	sub bx, 8
@@:
	in al, dx
	btr ax, bx
	out dx, al

;--- Setup DMA-controller
	call setdmaports
	.if bVerbose
		invoke printf, CStr("DMA ports addr/cnt/page=%X/%X/%X",lf), wDmaBaseChn, wDmaCntChn, wDmaPageChn
	.endif

;──────────────────────────────────────────────────────────────────────────────
; 1st  MASK DMA CHANNEL
;
	call GetDmaChannel
	or al, DMA_MASK_DISABLE_CHN	; bit 2 sets mask, bits 0-1 select channel
	mov dx, wDmaWriteMask
	out dx, al
;──────────────────────────────────────────────────────────────────────────────
; 2nd  CLEAR FLIPFLOP
;
	mov dx, wDmaClearFlipFlop
	out dx, al
;──────────────────────────────────────────────────────────────────────────────
; 3rd  WRITE TRANSFER MODE
;
	call GetDmaChannel
	or al, WANTEDMODE
	mov dx, wDmaWriteMode
	out dx, al
;──────────────────────────────────────────────────────────────────────────────
; 4th  WRITE PAGE NUMBER
;
	mov esi, [dwSampleBuffer]

	mov eax, esi
	shr eax, 16
;	cmp wDmaBaseChn, 10h
;	jb @F
;	shr eax, 1	; for 16-bit DMA, skip lowest bit
;@@:
	mov dx, wDmaPageChn
	out dx, al
;──────────────────────────────────────────────────────────────────────────────
; 5th  WRITE BASEADDRESS
;
	mov eax, esi
	mov dx, wDmaBaseChn
	cmp dx, 10h
	jb @F
	shr eax, 1	; for 16-bit DMA, skip lowest bit
@@:
	out dx, al
	mov al, ah
	out dx, al
;──────────────────────────────────────────────────────────────────────────────
; 6th  WRITE SAMPLELENGTH-1
;
	mov dx, wDmaCntChn
	mov ecx, SAMPLEBUFFERLENGTH
	cmp dx, 10h
	jb @F
	shr ecx, 1	; for 16-bit DMA, skip lowest bit
@@:
	dec ecx
	mov al, cl
	out dx, al
	mov al, ch
	out dx, al
;──────────────────────────────────────────────────────────────────────────────
; 7th  DEMASK CHANNEL
;
	call GetDmaChannel
	or al, DMA_MASK_ENABLE_CHN	; this is actually zero
	mov dx, wDmaWriteMask
	out dx, al

;──────────────────────────────────────────────────────────────────────────────
; Setup SoundBlaster :
;
; 1st  SET SAMPLERATE
;
	WriteDSP [wBase], DSP_SETOUTSAMPLERATE
	mov ecx, nSamplesPerSec
	WaitWrite
	mov al,ch
	out dx,al
	WaitWrite
	mov al,cl
	out dx,al
;──────────────────────────────────────────────────────────────────────────────
; 2nd  play 8/16bit stereo (B6 30)/mono (C6 00) XX XX
;
	WaitWrite
	mov ax, 030B6h				;B6,30 = DMA DAC 16bit autoinit, stereo, signed
	cmp wBitsPerSample, 16
	jz @F
	mov ax, 000C6h				;C6,00 = DMA DAC 8bit autoinit, mono, unsigned
@@:
	out dx, al
	WaitWrite
	mov al, ah					;AL = stereo signed / mono unsigned
	out dx, al
	call getsamplebufferlength
	dec ecx
	WaitWrite
	mov al, cl					; LOWER PART SAMPLELENGTH
	out dx, al
	WaitWrite
	mov al, ch					; HIGHER PART SAMPLELENGTH
	out dx, al
	clc
exit:
	ret

InitSB endp

;--- deinit SB hardware

ExitSB proc

	cmp bInit, 0
	jz exit    
	call ResetDSP
;--- restore PIC masks
	mov ax, wOldMask
	out 21h, al
	mov al, ah
	out 0A1h, al

;--- RESTORE IRQ
	mov al, byte ptr [wIrq]
	.if ( al < 8 )
		add al, 8
	.else
		add al, 68h
	.endif
	push ds
	lds dx, [OldIntSB]
	mov ah, 25h
	int 21h
	pop ds
exit:
	ret
ExitSB endp

