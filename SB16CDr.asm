	jnz cd_found
	invoke printf, CStr("no CD-ROM drive found",lf)
	jmp exit
cd_found:
	mov wDrive, cx

	mov req80.stasecs,0

	add argv, 2
	mov bx, argv
nexttrack:
	.while word ptr [bx]
		mov si, [bx]
		add bx, 2
		lodsw
		.if al >= '0' && al <= '9'
			sub si,2
			mov cl,10
			call getnum
			.if byte ptr [si]
				invoke printf, CStr("invalid digit: %s",lf), si
				jmp exit
			.endif
			mov track, al
			.break
		.elseif al == '/' || al == '-'
			or ah,20h
			.if ah == "q"
				mov bVerbose, 0
			.else
				mov bHelp, 1
			.endif
		.else
			mov bHelp, 1
		.endif
	.endw
	mov argv, bx

	cmp bHelp, 0
	jz @F
	invoke printf, CStr("%s"), offset szHelp
	jmp exit
@@:

;--- get volume size info (# of sectors)

	invoke getvolsize
	jc exit
	mov dwSectors, eax

;--- get TOC info ( first track, last track) in AL/AH
	invoke getdiskinfo
	mov lasttrack, ah

	.if track
		invoke gettrackinfo, track
		jc exit
		mov req80.stasecs, eax
		movzx ax, track
		.if al < lasttrack
			inc al
			invoke gettrackinfo, ax
			mov dwSectors, eax
		.endif
	.endif

	.if bVerbose
		invoke printf, CStr("SB base=%X, irq=%u, dma=%u, hdma=%u",lf), wBase, wIrq, wDmaL, wDmaH
	.endif

;--- set linear address (dwSampleBuffer) and offset (pSampleBuffer) of sample buffer

	call getsamplebufferaddr
	jc exit
	.if bVerbose
		invoke printf, CStr("sample buffer linear address=%lX",lf), [dwSampleBuffer]
	.endif

;--- fill sample buffer

	mov si, [pSampleBuffer]
	invoke cdread, si
	jc exit
	invoke cdread, si
	jc exit

;--- init SB hardware

	call InitSB
	jc exit

	call gettimer
	mov dwTimer, eax
waitloop:
	.if bVerbose
		call gettimer
		sub eax, dwTimer
		cmp eax, 5
		jb @F
		add dwTimer, eax
		invoke printf, CStr("curr sector: %lu",13), req80.stasecs
@@:
	.endif
	mov ah,01			; key pressed?
	int 16h
	jz @F
	mov ah,0
	int 16h
	cmp ah,1
	jz exit
@@:
	cmp [bReady],0		; SB interrupt occured?
	jz waitloop
	mov [bReady],0
	mov eax, dwSectors
	cmp eax, req80.stasecs
	jbe done
	invoke cdread, pSampleBuffer
	jc exit
	jmp waitloop
done:
;--- wait till the last half has been played
@@:
	cmp [bReady],0
	jz @B
	.if bVerbose
		invoke printf, CStr(10)
	.endif
	mov bx, argv
	cmp word ptr [bx],0
	jnz nexttrack
exit:
	call ExitSB
	ret

main endp

	include setargv.inc

start:
	mov ax,@data
	mov ds,ax
	mov cx,ss
	sub cx,ax
	shl cx,4
	mov ss,ax
	add sp,cx
	call _setargv
	invoke main, [_argc], [_argv]
	mov ax,04c00h
	int 21h

	END start
