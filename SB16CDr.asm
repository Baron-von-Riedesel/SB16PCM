WaitRead macro
LOCAL loopWait
	push cx
	xor cx, cx
loopWait:
	in al, dx
	test al,80h
	loopzw loopWait		; Jump if bit7=0 - no data available
	pop cx
endm

WaitWrite macro
LOCAL loopWait
	push cx
	xor cx,cx
loopWait:
	in al, dx
	test al, 80h
	loopnzw loopWait	; Jump if bit7=1 - writing not allowed
	pop cx
endm

WriteDSP macro Base, cmd
	mov dx, Base
	add dx, SB_DSPWRITE
	WaitWrite
	mov al, cmd
	out dx, al
endm

